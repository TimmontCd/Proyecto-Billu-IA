      EXECUTE IMMEDIATE p_create_sql;
      DBMS_OUTPUT.PUT_LINE('Created index ' || p_index_name);
    ELSE
      DBMS_OUTPUT.PUT_LINE('Index already exists ' || p_index_name);
    END IF;
  END;
