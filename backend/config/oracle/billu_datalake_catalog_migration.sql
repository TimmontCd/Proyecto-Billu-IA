  PROCEDURE ensure_column(p_table_name IN VARCHAR2, p_column_name IN VARCHAR2,
      p_column_definition IN VARCHAR2) IS
    v_count NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_TAB_COLUMNS
     WHERE TABLE_NAME = UPPER(p_table_name)
       AND COLUMN_NAME = UPPER(p_column_name);

    IF v_count = 0 THEN
      EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table_name || ' ADD ('
          || p_column_name || ' ' || p_column_definition || ')';
      DBMS_OUTPUT.PUT_LINE('Added column ' || p_table_name || '.' || p_column_name);
    ELSE
      DBMS_OUTPUT.PUT_LINE('Column already exists ' || p_table_name || '.' || p_column_name);
    END IF;
  END;

  PROCEDURE ensure_table(p_table_name IN VARCHAR2, p_create_sql IN VARCHAR2) IS
    v_count NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_TABLES
     WHERE TABLE_NAME = UPPER(p_table_name);

    IF v_count = 0 THEN
      EXECUTE IMMEDIATE p_create_sql;
      DBMS_OUTPUT.PUT_LINE('Created table ' || p_table_name);
    ELSE
      DBMS_OUTPUT.PUT_LINE('Table already exists ' || p_table_name);
    END IF;
  END;

  PROCEDURE ensure_index(p_index_name IN VARCHAR2, p_create_sql IN VARCHAR2) IS
    v_count NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_INDEXES
     WHERE INDEX_NAME = UPPER(p_index_name);

