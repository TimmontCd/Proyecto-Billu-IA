  SOURCE_SPREADSHEET_ID VARCHAR2(128 CHAR),
  SOURCE_SHEET_NAME VARCHAR2(128 CHAR),
  INGESTION_BATCH_ID VARCHAR2(64 CHAR) NOT NULL,
  LOADED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_DLK_CARD_PHYS CHECK (HAS_PHYSICAL_CARD_FLAG IN ('Y', 'N')),
  CONSTRAINT CK_DLK_CARD_DIG CHECK (HAS_DIGITAL_CARD_FLAG IN ('Y', 'N')),
  CONSTRAINT CK_DLK_CARD_RPH CHECK (HAS_RECENT_PHYSICAL_USAGE_FLAG IN ('Y', 'N')),
  CONSTRAINT CK_DLK_CARD_RDI CHECK (HAS_RECENT_DIGITAL_USAGE_FLAG IN ('Y', 'N')),
  CONSTRAINT UK_DLK_CARD_STATUS UNIQUE (REWARDS_ID, CUT_DATE)
);

CREATE INDEX IX_DLK_CARD_STATUS_01 ON DLK_CARD_STATUS (CUT_DATE, REWARDS_ID);

