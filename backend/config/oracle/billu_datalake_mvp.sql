CREATE TABLE DLK_CUSTOMER (
  CUSTOMER_SK NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  REWARDS_ID VARCHAR2(64 CHAR) NOT NULL,
  ACCOUNT_STATUS VARCHAR2(40 CHAR),
  OPENING_DATE DATE,
  STATE_NAME VARCHAR2(120 CHAR),
  MUNICIPALITY_NAME VARCHAR2(120 CHAR),
  GENDER_CODE VARCHAR2(10 CHAR),
  BIRTH_DATE DATE,
  AGE_YEARS NUMBER(3),
  SOURCE_SYSTEM VARCHAR2(40 CHAR) DEFAULT 'LEGACY_GSHEET' NOT NULL,
  SOURCE_SPREADSHEET_ID VARCHAR2(128 CHAR),
  SOURCE_SHEET_NAME VARCHAR2(128 CHAR),
  INGESTION_BATCH_ID VARCHAR2(64 CHAR) NOT NULL,
  EFFECTIVE_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT UK_DLK_CUSTOMER UNIQUE (REWARDS_ID)
);

CREATE INDEX IX_DLK_CUSTOMER_01 ON DLK_CUSTOMER (ACCOUNT_STATUS, STATE_NAME);

CREATE TABLE DLK_CUSTOMER_PRODUCT_SNAPSHOT (
  CUSTOMER_PRODUCT_SK NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  REWARDS_ID VARCHAR2(64 CHAR) NOT NULL,
  SNAPSHOT_DATE DATE NOT NULL,
  PRODUCT_CODE VARCHAR2(40 CHAR) NOT NULL,
  PRODUCT_LABEL VARCHAR2(120 CHAR),
  PRODUCT_ACTIVE_FLAG CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
  PRODUCT_BALANCE_AMOUNT NUMBER(18,2) DEFAULT 0 NOT NULL,
  ACCOUNT_STATUS VARCHAR2(40 CHAR),
  SOURCE_SYSTEM VARCHAR2(40 CHAR) DEFAULT 'LEGACY_GSHEET' NOT NULL,
  INGESTION_BATCH_ID VARCHAR2(64 CHAR) NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_DLK_CUST_PROD_FLAG CHECK (PRODUCT_ACTIVE_FLAG IN ('Y', 'N')),
  CONSTRAINT UK_DLK_CUST_PROD UNIQUE (REWARDS_ID, SNAPSHOT_DATE, PRODUCT_CODE)
);

CREATE INDEX IX_DLK_CUST_PROD_01 ON DLK_CUSTOMER_PRODUCT_SNAPSHOT (REWARDS_ID, SNAPSHOT_DATE);
CREATE INDEX IX_DLK_CUST_PROD_02 ON DLK_CUSTOMER_PRODUCT_SNAPSHOT (PRODUCT_CODE, SNAPSHOT_DATE);

CREATE TABLE DLK_MONTHLY_BALANCE (
  MONTHLY_BALANCE_SK NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  REWARDS_ID VARCHAR2(64 CHAR) NOT NULL,
  BALANCE_MONTH DATE NOT NULL,
  BALANCE_AMOUNT NUMBER(18,2) DEFAULT 0 NOT NULL,
  MONTH_HEADER_LABEL VARCHAR2(80 CHAR),
  IS_IMPUTED_FLAG CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  SOURCE_SYSTEM VARCHAR2(40 CHAR) DEFAULT 'LEGACY_GSHEET' NOT NULL,
  SOURCE_SPREADSHEET_ID VARCHAR2(128 CHAR),
  SOURCE_SHEET_NAME VARCHAR2(128 CHAR),
  INGESTION_BATCH_ID VARCHAR2(64 CHAR) NOT NULL,
  LOADED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_DLK_MONTHLY_BAL_IMPUTED CHECK (IS_IMPUTED_FLAG IN ('Y', 'N')),
  CONSTRAINT UK_DLK_MONTHLY_BALANCE UNIQUE (REWARDS_ID, BALANCE_MONTH)
);

CREATE INDEX IX_DLK_MONTHLY_BALANCE_01 ON DLK_MONTHLY_BALANCE (BALANCE_MONTH, REWARDS_ID);

CREATE TABLE DLK_TRANSACTION (
  TRANSACTION_SK NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  REWARDS_ID VARCHAR2(64 CHAR) NOT NULL,
  TRANSACTION_DATE DATE NOT NULL,
  ABONO_AMOUNT NUMBER(18,2) DEFAULT 0 NOT NULL,
  CARGO_AMOUNT NUMBER(18,2) DEFAULT 0 NOT NULL,
  DESCRIPTION_TEXT VARCHAR2(400 CHAR),
  DESCRIPTION_CODE_TEXT VARCHAR2(400 CHAR),
  OPERATOR_TEXT VARCHAR2(200 CHAR),
  ECOMMERCE_FLAG CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  PHYSICAL_STORE_FLAG CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
  MERCHANT_CATEGORY_CODE VARCHAR2(80 CHAR),
  MERCHANT_CATEGORY_DESC VARCHAR2(200 CHAR),
  SOURCE_MONTH VARCHAR2(20 CHAR),
  SOURCE_FILE_LABEL VARCHAR2(200 CHAR),
  SOURCE_SPREADSHEET_ID VARCHAR2(128 CHAR),
  SOURCE_SHEET_ID NUMBER,
  INGESTION_BATCH_ID VARCHAR2(64 CHAR) NOT NULL,
  LOADED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_DLK_TX_ECOM CHECK (ECOMMERCE_FLAG IN ('Y', 'N')),
  CONSTRAINT CK_DLK_TX_STORE CHECK (PHYSICAL_STORE_FLAG IN ('Y', 'N'))
);

CREATE INDEX IX_DLK_TRANSACTION_01 ON DLK_TRANSACTION (REWARDS_ID, TRANSACTION_DATE);
CREATE INDEX IX_DLK_TRANSACTION_02 ON DLK_TRANSACTION (TRANSACTION_DATE);
CREATE INDEX IX_DLK_TRANSACTION_03 ON DLK_TRANSACTION (MERCHANT_CATEGORY_CODE);

CREATE TABLE DLK_CARD_STATUS (
  CARD_STATUS_SK NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
